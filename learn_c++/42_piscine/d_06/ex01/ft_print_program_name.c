#include <unistd.h>

int		ft_putchar(char c)
{
	write(1, &c, 1);
	return (0);
}

int		main(int argc, char **argv)
{
	int i;

	while (argv[0][i] != '\0')
		ft_putchar(argv[0][i++]);
	ft_putchar('\n');
	return (0);
}
