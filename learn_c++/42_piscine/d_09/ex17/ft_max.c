int		ft_max(int *tab, int length)
{
	int i;
	int ma;

	if (!tab || length == 0)
		return (0);
	i = 0;
	ma = -2147483648;
	while (i < length)
	{
		if (tab[i] > ma)
			ma = tab[i];
		i++;
	}
	return (ma);
}
