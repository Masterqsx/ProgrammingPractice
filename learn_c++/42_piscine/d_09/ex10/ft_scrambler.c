void	swap(int *a, int *b)
{
	*a ^= *b ^ (*b = *a);
}

void	ft_scrambler(int ***a, int *b, int *******c, int ****d)
{
	swap(**a, ******c);
	swap(**a, ***d);
	swap(**a, b);
}
