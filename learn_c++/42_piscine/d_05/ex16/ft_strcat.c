char	*ft_strcat(char *dest, char *src)
{
	int i;
	int j;

	if (!dest || !src)
		return (dest);
	i = 0;
	while (dest[i] != '\0')
		i++;
	j = 0;
	while (src[j] != '\0')
		dest[i++] = src[j++];
	dest[i] = '\0';
	return (dest);
}
