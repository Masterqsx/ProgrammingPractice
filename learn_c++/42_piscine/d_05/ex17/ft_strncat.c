char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int j;

	if (!dest || !src)
		return (dest);
	i = 0;
	while (dest[i] != '\0')
		i++;
	j = 0;
	while (src[j] != '\0' && (j < nb || nb < 0))
		dest[i++] = src[j++];
	dest[i] = '\0';
	return (dest);
}
