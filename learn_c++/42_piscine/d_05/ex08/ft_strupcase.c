char	*ft_strupcase(char *str)
{
	int i;

	if (!str)
		return (0);
	i = 0;
	while (str[i++] != '\0')
	{
		if (str[i - 1] >= 'a' && str[i - 1] <= 'z')
			str[i - 1] += 'A' - 'a';
	}
	return (str);
}
