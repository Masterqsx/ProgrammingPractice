char	*ft_strlowcase(char *str)
{
	int i;

	if (!str)
		return (0);
	i = 0;
	while (str[i++] != '\0')
	{
		if (str[i - 1] >= 'A' && str[i - 1] <= 'Z')
			str[i - 1] += 'a' - 'A';
	}
	return (str);
}
