char* ft_strrev(char* str)
{
    unsigned int i,j; 
    
    if(!str) return str;
    i = 0;
    j = 0;
    while(str[j] != '\0') j++;
    j--;
    while(i < j)
    {
        str[i] ^= str[j] ^ (str[j] = str[i]);
        ++i;
        --j;
    }
    return str;
}

