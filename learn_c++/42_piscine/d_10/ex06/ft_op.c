int		ad(int i, int j)
{
	return (i + j);
}

int		mi(int i, int j)
{
	return (i - j);
}

int		mu(int i, int j)
{
	return (i * j);
}

int		di(int i, int j)
{
	return (i / j);
}

int		mo(int i, int j)
{
	return (i % j);
}
