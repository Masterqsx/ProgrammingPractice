#include <stdlib.h>

int		*ft_map(int *tab, int length, int (*f) (int))
{
	int i;
	int *res;

	if (!tab || !f)
		return (0);
	if ((res = (int *)malloc(length * sizeof(*res))) == 0)
		return (0);
	i = 0;
	while (i < length)
	{
		res[i] = f(tab[i]);
		i++;
	}
	return (res);
}
