int	ft_is_prime(int nb)
{
	int divider;

	if (nb % 2 == 0 || nb < 2)
		return (0);
	if ((nb / 2) % 2 == 0)
		divider = nb / 2 + 1;
	else
		divider = nb / 2;
	while (divider > 1)
	{
		if (nb % divider == 0)
			return (0);
		divider -= 2;
	}
	return (1);
}
